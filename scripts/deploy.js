const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const messageContractFactory = await hre.ethers.getContractFactory("MessageBoard");
    const messageContract = await messageContractFactory.deploy();
    await messageContract.deployed();
  
  
    console.log("MessageBoard address: ", messageContract.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();